"""
Waitlist — pre-launch email capture for the access gate.

Endpoints
---------
POST /api/waitlist          — unauth'd; { email, source? } → creates/updates
GET  /api/waitlist/count    — unauth'd; { count: N }

Launch-day hardening
--------------------
• Confirmation email via Resend (with the "Using Marvex Studio" PDF attached)
  fires after every NEW signup. Re-submits don't re-send the email.
• Per-IP rate limit: at most 5 signups per minute from the same address,
  enforced via an in-memory sliding-window counter. Survives the typical
  share-on-Reddit traffic spike without needing Redis.
• Email failures NEVER block the API response — we always tell the user
  "you're on the list" and log the email error for our own debugging.

Design notes
------------
• Deduplicates by email (normalised, lowercase). Second submit updates the
  source field but doesn't double-count and doesn't re-email.
• The admin exports via `db.waitlist.find()` when ready to launch.
"""

import asyncio
import logging
import re
import time
from collections import defaultdict, deque
from datetime import datetime, timezone
from pathlib import Path
from typing import Optional

from fastapi import APIRouter, HTTPException, Request
from motor.motor_asyncio import AsyncIOMotorDatabase
from pydantic import BaseModel, Field

from email_sender import send_email

logger = logging.getLogger(__name__)

EMAIL_RE = re.compile(r"^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$")

# Per-IP sliding window (in seconds). Cheap and adequate for a launch day.
_RATE_WINDOW_S = 60
_RATE_MAX_HITS = 5
_recent_hits: dict[str, deque] = defaultdict(deque)

# Welcome PDF path — generated by /app/backend/scripts/build_welcome_pdf.py
WELCOME_PDF = Path(__file__).parent / "assets" / "using-marvex.pdf"


def _client_ip(req: Request) -> str:
    """Best-effort client IP. Honours the X-Forwarded-For header that the
    Kubernetes ingress prepends, falls back to the direct peer."""
    xff = req.headers.get("x-forwarded-for", "")
    if xff:
        # XFF is a comma-separated list — the first entry is the original.
        return xff.split(",")[0].strip()
    return req.client.host if req.client else "unknown"


def _rate_limited(ip: str) -> bool:
    now = time.time()
    bucket = _recent_hits[ip]
    # Evict timestamps older than the window
    while bucket and now - bucket[0] > _RATE_WINDOW_S:
        bucket.popleft()
    if len(bucket) >= _RATE_MAX_HITS:
        return True
    bucket.append(now)
    return False


