class WaitlistJoin(BaseModel):
    email: str = Field(..., max_length=320)
    source: Optional[str] = Field(None, max_length=80)


def make_router(db: AsyncIOMotorDatabase) -> APIRouter:
    router = APIRouter(prefix="/api/waitlist")

    @router.post("")
    async def join(payload: WaitlistJoin, request: Request):
        ip = _client_ip(request)
        if _rate_limited(ip):
            logger.warning("waitlist rate-limit hit from %s", ip)
            raise HTTPException(
                status_code=429,
                detail="You're going a little fast. Please try again in a minute.",
            )

        email = payload.email.strip().lower()
        if not EMAIL_RE.match(email):
            raise HTTPException(status_code=400, detail="Please enter a valid email address")

        now = datetime.now(timezone.utc)
        # Atomic upsert lets us tell whether this is a NEW signup (and
        # therefore whether to send the welcome email) without a race
        # window between read and write.
        result = await db.waitlist.update_one(
            {"email": email},
            {
                "$setOnInsert": {"created_at": now, "welcomed": False},
                "$set": {"source": (payload.source or "landing")[:80], "last_seen": now},
            },
            upsert=True,
        )
        is_new = result.upserted_id is not None
        total = await db.waitlist.count_documents({})

        # Fire the welcome email asynchronously — never block the response on
        # email delivery. If it succeeds, mark `welcomed=True` so we don't
        # re-send on subsequent submits even if the user clears their
        # localStorage and rejoins.
        if is_new:
            asyncio.create_task(_send_welcome(db, email))

        return {"ok": True, "count": total, "new": is_new}

    @router.get("/count")
    async def count():
        total = await db.waitlist.count_documents({})
        return {"count": total}

    return router


async def _send_welcome(db: AsyncIOMotorDatabase, email: str) -> None:
    """Background task: send the confirmation email + PDF, then record the
    delivery status on the user's waitlist row.

    `reply_to` defaults to `support@marvex.app` so anyone who replies
    to the welcome email lands in the support helper's mailbox, not in
    the no-reply Resend address. Override via `WAITLIST_REPLY_TO` env
    if your routing changes.
    """
    import os as _os
    reply_to = _os.environ.get("WAITLIST_REPLY_TO") or "support@marvex.app"
    try:
        result = await send_email(
            to=email,
            subject="Welcome to Marvex Studio — your starter guide is inside",
            html=_welcome_html(email),
            attachments=[WELCOME_PDF] if WELCOME_PDF.exists() else None,
            reply_to=reply_to,
        )
        await db.waitlist.update_one(
            {"email": email},
            {"$set": {
                "welcomed": bool(result.get("ok")),
                "welcome_status": result,
                "welcomed_at": datetime.now(timezone.utc),
            }},
        )
    except Exception:
        logger.exception("welcome-email background task crashed for %s", email)
